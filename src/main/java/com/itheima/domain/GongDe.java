package com.itheima.domain;

public class GongDe {
    Integer host;
    Integer friend;

    public GongDe(Integer host, Integer friend) {
        this.host = host;
        this.friend = friend;
    }

    public Integer getHost() {
        return host;
    }

    public void setHost(Integer host) {
        this.host = host;
    }

    public Integer getFriend() {
        return friend;
    }

    public void setFriend(Integer friend) {
        this.friend = friend;
    }
}
